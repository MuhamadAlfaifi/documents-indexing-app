     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $validated = $request->validate([
            'enabled' => 'boolean',
        ]);

        $user->forceFill($validated)->save();

        return redirect()->back();
